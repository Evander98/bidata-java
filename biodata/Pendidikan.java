package biodata;

public class Pendidikan {
  private String sd;
  private String smp;
  private String sma;
  private int tahunLulusSd;
  private int tahunLulusSmp;
  private int tahunLulusSma;
  
  public String getSd() {
    return sd;
  }

  public void setSd(String sd) {
    this.sd = sd;
  }

  public String getSmp() {
    return smp;
  }

  public void setSmp(String smp) {
    this.smp = smp;
  }

  public String getSma() {
    return sma;
  }

  public void setSma(String sma) {
    this.sma = sma;
  }

  public int getTahunLulusSd() {
    return tahunLulusSd;
  }

  public void setTahunLulusSd(int tahunLulusSd) {
    this.tahunLulusSd = tahunLulusSd;
  }

  public int getTahunLulusSmp() {
    return tahunLulusSmp;
  }

  public void setTahunLulusSmp(int tahunLulusSmp) {
    this.tahunLulusSmp = tahunLulusSmp;
  }

  public int getTahunLulusSma() {
    return tahunLulusSma;
  }

  public void setTahunLulusSma(int tahunLulusSma) {
    this.tahunLulusSma = tahunLulusSma;
  }

  
}
